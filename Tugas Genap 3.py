# (di urutan 8) rumus luas bola = 4 x π x r²
# (di urutan 9) rumus volume bola = 4/3 x π x r³

r = int(input("Jari-jari Bola : "))
π = 3.14

luas = int(4 * π * (r * r))
volume = int((4 / 3) * π * (r * r * r))

print("Luas Bola = ", +(luas))
print("Volume Bola = ", +(volume))
